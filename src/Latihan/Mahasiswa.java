package Latihan;
public class Mahasiswa {
    private String nama;
    private String nomorInduk;
    public String getNama() {
        return nama;
    }
    public void setNama(String nama) {
        this.nama = nama;
    }
    public String getNomorInduk() {
        return nomorInduk;
    }
    public void setNomorInduk(String nomorInduk) {
        this.nomorInduk = nomorInduk;
    }
}
